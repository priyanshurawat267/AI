## string methods
str = "future of AI is the future of humanity!"
print("1 :", str.lower()) ## Converts the string to lowercase
print("1 :", str.lower()) ## Converts the string to lowercase
print("1 :", str.lower()) ## Converts the string to lowercase
print("1 :", str.lower()) ## Converts the string to lowercase
print("1 :", str.lower()) ## Converts the string to lowercase
print("2 :", str.upper()) ## Converts the string to uppercase
print("3 :", str.islower()) ## Checks if the string is in lowercase
print("4 :", str.isupper()) ## Checks if the string is in uppercase
print("5 :", str.isalpha()) ## Checks if all characters in the string are alphabetic
print("6 :", str.isdigit()) ## Checks if all characters in the string are digits
print("7 :", str.split()) ## Splits the string into a list of substrings
print("8 :", str.replace("A", "B")) ## Replaces all occurrences of a substring with another substring
print("9 :", str.strip(" ")) ## Removes leading and trailing whitespace
print("10 :", str.rstrip("!")) ## Removes trailing whitespace and specified characters
print("11 :", str.lstrip("A")) ## Removes leading whitespace and specified characters
print("12 :", str.find("the")) ## Returns the index of the first occurrence of a substring
print("13 :", str.count("e")) ## Returns the number of occurrences of a substring
print("14 :", str.startswith("AI")) ## Checks if the string starts with the specified value
print("15 :", str.endswith("!")) ## Checks if the string ends with the specified value
print("16 :", str.index("the")) ## Returns the index of the first occurrence of a substring
print("17 :", str.capitalize()) ## Converts the first character to uppercase and the rest to lowercase
print("18 :", str.title()) ## Converts the string to title case
print("19 :", str.swapcase()) ## Swaps the case of all characters in the string
print("20 :", str.center(30, "*")) ## Centers the string in a field of specified width
print("21 :", str.zfill(30)) ## Pads the string with zeros on the left
print("22 :", str.isalnum()) ## Checks if all characters in the string are alphanumeric
print("23 :", str.isprintable()) ## Checks if all characters in the string are printable
print("24 :", str.isidentifier()) ## Checks if the string is a valid identifier
print("25 :", str.isnumeric()) ## Checks if all characters in the string are numeric
print("26 :", str.isdecimal()) ## Checks if all characters in the string are decimal
print("27 :", str.isascii()) ## Checks if all characters in the string are ASCII
print("28 :", str.partition("the")) ## Splits the string into three parts
print("29 :", str.rpartition("the")) ## Splits the string into three parts from the right
print("30 :", str.join(["Hello", "World"])) ## Joins a list of strings with the string as separator
print("31 :", str.encode()) ## Encodes the string using the specified encoding
print("32 :", str.expandtabs()) ## Replaces tab characters with spaces
print("33 :", str.endswith("!")) ## Checks if the string ends with the specified value
print("34 :", str.isalnum()) ## Checks if all characters in the string are alphanumeric (A-Z, a-z, 0-9)
print("35 :", str.istitle()) ## Checks if the string is in title case
